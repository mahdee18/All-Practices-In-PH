var marks = 80;
if (marks >= 80) {
    console.log('His GPA is - A+')
}
else if (marks >= 70) {
    console.log('His GPA is - A')
}
else if (marks >= 60) {
    console.log('His GPA is - A-')
}
else if (marks >= 50) {
    console.log('His GPA is - B')
}
else if (marks >= 40) {
    console.log('His GPA is - C')
}
else {
    console.log('Tumi fail!')
}